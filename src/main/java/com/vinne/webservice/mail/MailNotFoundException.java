package com.vinne.webservice.mail;

public class MailNotFoundException extends Exception {

	
	private static final long serialVersionUID = 5602997301798204007L;

	public MailNotFoundException() {
		super();		
	}

	public MailNotFoundException(String message, Throwable cause) {
		super(message, cause);		
	}

	
	public MailNotFoundException(String arg0) {
		super(arg0);
		
	}
	
	

}
